package com.Kaushal.education.model;

public class User {
    private String name;
    private String email;

    // constructor
    public User() {}

    // getters and setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
